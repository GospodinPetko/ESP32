# Function to display the image
def display_image(oled, image_data):
    for page in range(8):  # Each page is 8 pixels tall (64 / 8 = 8 pages)
        for x in range(128):  # Width of the screen is 128 pixels
            byte = image_data[page * 128 + x]  # Get byte from image data
            for bit in range(8):  # For each bit in the byte
                pixel_on = (byte >> bit) & 0x01  # Check if the bit is set
                oled.pixel(x, page * 8 + bit, pixel_on)

# Clear the display
oled.fill(0)

# Display the image on OLED
display_image(oled, image_data)

# Show the image
oled.show()
